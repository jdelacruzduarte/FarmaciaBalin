 void cargar(String valor) {
        try{
            String [] titulos={"Codigo","Proveedor","Nombre Producto","Descripcion","Precio", "Stock"};
            String [] registros= new String[7];
            model=new DefaultTableModel(null,titulos);
           
            String cons="select * from productos";
            Statement st= cn.createStatement();
            ResultSet rs = st.executeQuery(cons);
            while(rs.next()){
                registros[0]=rs.getString("idProductos");
                registros[1]=rs.getString("idProveedor");
                registros[2]=rs.getString("NombreProductos");
                registros[3]=rs.getString("DescripcionProductos");
                registros[4]=rs.getString("PrecioProductos");
                registros[5]=rs.getString("Stock");
                model.addRow(registros);      
                }
            tableProductos.setModel(model);
            tableProductos.getColumnModel().getColumn(0).setPreferredWidth(150);
            tableProductos.getColumnModel().getColumn(1).setPreferredWidth(200);
            tableProductos.getColumnModel().getColumn(2).setPreferredWidth(150);
            tableProductos.getColumnModel().getColumn(3).setPreferredWidth(150);
            tableProductos.getColumnModel().getColumn(4).setPreferredWidth(150);
            }catch(Exception e){
                System.out.println(e.getMessage());
                 }     
    }
 void BuscarProductoEditar(String cod) {        
        try{
           //Codigo","Proveedor","Nombre Producto","Descripcion","Precio", "Stock"
            String codi="",prov="",nom="",desc="",prec="", stock="";
            String cons="select * from productos WHERE idProductos='"+cod+"'";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(cons);
            while(rs.next())
            {
            codi=rs.getString(1);
            prov=rs.getString(2);
            nom=rs.getString(3);
            desc=rs.getString(4);
            prec=rs.getString(5);
            stock=rs.getString(6);
           
            }
            txtCodigo.setText(codi);
            txtNombre.setText(nom);
            txtPrecio.setText(prec);
            txtStock.setText(stock);
            txtDescripcion.setText(desc);
            comboBoxProducto.setSelectedItem(prov);          
           
            }catch(Exception e)
            {
            System.out.println(e.getMessage());
            }     
    }
